class Vessels{

		static int vesselId ;
		static String brand ;
		static String material ;
		static int  capacity ;
		
		static void setVesselsId(int vId){
			vesselId = vId ;
		}
		static int getVesselsId(){
			return vesselId ;
		}
		
		static void setBrand(String br){
			brand = br ;
		}
		static String getBrand(){
			return brand ;
		}
		
		static void setMaterial(String matl){
			material = matl ;
		}
		static String getMaterial(){
			return material ;
		}
		
		static void setCapacity(int cap){
			capacity = cap ;
		}
		static int getCapacity(){
			return capacity ;
		}






}